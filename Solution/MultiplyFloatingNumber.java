package Solution;
//Multiple Floating Number

import java.util.Scanner;

public class MultiplyFloatingNumber {
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		long f1=s.nextLong();
		long f2=s.nextLong();
		long ans=f1*f2;
		System.out.println("product of two long Integer is:"+ans);
	}
}
